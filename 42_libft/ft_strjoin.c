#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)
{
	char	*result;
	char	*result_ptr;

	result = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	result_ptr = result;
	if (!result)
		return (NULL);
	while (*s1)
		*result++ = *s1++;
	while (*s2)
		*result++ = *s2++;
	*result = '\0';
	return (result_ptr);
}
